/****** Script for SelectTopNRows command from SSMS  ******/
CREATE view [dbo].[anytone] as

SELECT '' AS [No.]	  
	  ,[RADIO_ID] AS [Radio ID]
      ,[CALLSIGN] AS [Callsign]
      ,CONCAT([FIRST_NAME], ' ', [LAST_NAME]) AS [Name]
      ,[CITY] AS [City]
      ,[STATE] AS [State]
      ,[COUNTRY] AS [Country]
      ,'' AS [Remarks]
	  ,'' AS [Call Type]
	  ,'' AS [Call Alert]
  FROM [DMR].[dbo].[user]

GO
