------------------------- Create ProScan View -------------------------

USE [DMR]
GO

/****** Object:  View [dbo].[proscan]    Script Date: 4/18/2020 12:16:47 ******/
SET ANSI_NULLS ON
GO

SET QUOTED_IDENTIFIER ON
GO





CREATE view [dbo].[proscan] as 
SELECT REPLACE(rtrim(CONCAT(REPLACE([RADIO_ID], ' ', '')
	  , ' LOGTEXT='
      ,REPLACE([CALLSIGN], ' ', '')
	  ,' '
      ,REPLACE([FIRST_NAME], ' ', '')
	  ,' '
      ,REPLACE([LAST_NAME], ' ', '')
	  ,' '
	  ,REPLACE([STATE], ' ', ''))), '  ', ' ') AS [#SQL]
  FROM [dbo].[user]
  where len([RADIO_ID]) > 0

GO


